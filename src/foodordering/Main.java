    private void removebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removebtnActionPerformed
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/store", "root", "");

            int i = Integer.parseInt(sumtxt.getText());
            int ii = Integer.parseInt(pricetxt.getText());
            int t = i - ii;
            String f = String.valueOf(t);
            sumtxt.setText(f);

            String id = idtxt.getText();

            if (id.equals(0)) {
                JOptionPane.showMessageDialog(null, "No Data Selected!");
            } else {
                PreparedStatement ps = con.prepareStatement("DELETE FROM cart where id = '" + id + "'");
                ps.execute();
                loadrecord1(jTable2);
                con.close();
                idtxt.setText("0");
                producttxt.setText("");
                pricetxt.setText("");
            }

        } catch (Exception e) {
            System.err.println(e.getMessage());
        }
        removebtn.setEnabled(false);
    }//GEN-LAST:event_removebtnActionPerformed

