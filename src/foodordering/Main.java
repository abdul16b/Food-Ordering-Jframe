public void loadrecord(JTable table) {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/store", "root", "");
            PreparedStatement ps = con.prepareStatement("SELECT * FROM product");
            ResultSet rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            model.setRowCount(0);
            Object[] row;
            while (rs.next()) {
                row = new Object[3];
                row[0] = rs.getInt(1);
                row[1] = rs.getString(2);
                row[2] = rs.getInt(3);
                model.addRow(row);
            }
            con.close();
        } catch (Exception e) {
            System.err.println(e.getMessage());
        }
    }

    public void loadrecord1(JTable table) {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/store", "root", "");
            PreparedStatement ps = con.prepareStatement("SELECT * FROM cart");
            ResultSet rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            model.setRowCount(0);
            Object[] row;
            while (rs.next()) {
                row = new Object[4];
                row[0] = rs.getInt(1);
                row[1] = rs.getString(2);
                row[2] = rs.getString(3);
                row[3] = rs.getInt(4);
                model.addRow(row);
            }
            con.close();
        } catch (Exception e) {
            System.err.println(e.getMessage());
        }
    }

    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        String id = idtxt.getText();
        String product = producttxt.getText();
        String price = pricetxt.getText();

        int prices = Integer.parseInt(price);
        int qty = Integer.parseInt(qtytxt.getText());
        int total = prices * qty;
        String tot = String.valueOf(total);
        String Quantity = String.valueOf(qty);

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/store", "root", "");

            PreparedStatement ps = con.prepareStatement("INSERT INTO cart (id, product, quantity, price) values (?,?,?,?)");
            ps.setString(1, id);
            ps.setString(2, product);
            ps.setString(3, Quantity);
            ps.setString(4, tot);
            ps.execute();
            con.close();
            loadrecord(jTable1);
            idtxt.setText("0");
            producttxt.setText("");
            pricetxt.setText("");
            qtytxt.setText("");
            loadrecord1(jTable2);
            int sum = Integer.parseInt(sumtxt.getText());
            int over = sum + total;
            String t = String.valueOf(over);
            sumtxt.setText(t);
        } catch (Exception e) {
            System.err.println(e.getMessage());
        }
        paybtn.setEnabled(true);
        addbtn.setEnabled(false);
    }//GEN-LAST:event_addbtnActionPerformed

